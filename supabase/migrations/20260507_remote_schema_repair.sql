create extension if not exists pgcrypto;

create or replace function public.bump_sync_metadata()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
begin
  new.updated_at := now();

  if tg_op = 'UPDATE' then
    new.version := old.version + 1;
  elsif new.version is null then
    new.version := 1;
  end if;

  if new.sync_id is null then
    new.sync_id := gen_random_uuid();
  end if;

  return new;
end;
$$;

create or replace function public.get_server_time()
returns timestamptz
language sql
stable
set search_path = pg_catalog
as $$
  select now();
$$;

do $$
begin
  if to_regprocedure('public.rls_auto_enable()') is not null then
    revoke execute on function public.rls_auto_enable() from public, anon, authenticated;
  end if;
end;
$$;
revoke execute on function public.bump_sync_metadata() from public, anon, authenticated;

